        <h1>Genèse</h1>
        <p>
            L'association DEÏS MUSIQUE, présidée par Roger DRUI, a été créée en janvier 1994. A l'origine du projet, le groupe DEÏS, formé de cinq musiciens rock qui, ayant trouvé une salle de répétitions, ont décidé de mettre en commun leurs finances et sont partis pour une série de concerts. L'intérêt de créer une association était de souder les membres entre eux autour d'un projet fédérateur et bénéficier d'une structure juridique, contracter une assurance et gérer un budget en commun afin d'acheter du matériel, régler le loyer de la salle de répétition, etc...
            L'école de musique est née deux ans après.
        </p>


        <h1>éthique</h1>
        <p>
            La démarche des fondateurs, tous musiciens autodidactes, a été de trouver une alternative aux méthodes d'enseignement traditionnelles austères afin de pouvoir offrir à tous, novices ou expérimentés, le plaisir de pratiquer l'instrument ou le chant de leur choix de façon ludique et à leur rythme. Car le plaisir de jouer est le moteur essentiel pour apprendre vite, ajouté à un peu d'opiniâtreté et un bon suivi.
            Tous les genres de musique sont enseignés, du classique au rock et à la variété, pour permettre à chacun de jouer son style préféré.
            L' association s'investit beaucoup dans chaque élève et les aide à atteindre leurs objectifs personnel (formation de groupe, projet de concert, écriture et arrangement de composition...). Notre école forme les élèves de tout niveau, du débutant au confirmé en cours individuels et les aide à évoluer à leur rythme. Il y a une certaine souplesse dans les cours dispensés qui plaît ; par exemple, le solfège est intégré progressivement en même temps que l'apprentissage de l'instrument et ne fait pas l'objet d'une étude séparée qui peut rebuter.
            Afin de partager les progrès accomplis avec leurs proches, tous les ans en juin et pour fêter l'été, a lieu une audition de fin d'année durant laquelle les élèves jouent en solo ou en ensemble des oeuvres diverses qui sont toujours très applaudies.
            L'association aide également les artistes à réaliser une maquette de leurs créations avec l'appui technique d'un home-studio.
        </p>

