        <h2>Le saxophone</h2>
        <p>
            Inventé par Adolphe Sax au XIXème siècle, relativement facile à apprendre, symbole à lui seul du jazz et de l'improvisation. Aussi à l'aise dans le rock que le classique (réécoutez le Boléro de Ravel : c'est encore lui ce son plein de richesse !), il est sans doute l'un des instruments les plus expressifs qui soient, un remède contre les états d'âme...
        </p>
        <h2>Le violon</h2>
        <p>
            Présent dans les musiques populaires du monde entier (tzigane, arabo andalou,folk...) ce symbole de l'universalité est également le plus ancien des instruments modernes. Halte aux idées reçues : il n'est pas plus compliqué à jouer que les autres, son coût équivaut à celui d'une guitare et il s'apprend aisément dès 4 ans !
        </p>
        <h2>La flûte traversière</h2>
        <p>
