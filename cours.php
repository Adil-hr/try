        </p>
        <h2>Le violoncelle</h2>
        <p>
            On le dit le plus proche de la voix humaine... Il est également et peut-être pour ces raisons celui qui parle le mieux à nos états d'âme. Instrument intimiste mais aussi à l'aise en duo qu'en orchestre, il saura vous accompagner toute en volupté...
        </p>
